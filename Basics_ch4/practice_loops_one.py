#create a list of three favorite types of pizza store these values and iterate them each, print each pizza

pizzas = ['margarita', 'pepperoni', 'supreme']

#iterating over each pizza and printing a phrase including each name.
for pizza in pizzas:
    print(f"{pizza.title()} pizza is great! \n")

#now think of three different animals in a list store these values and use for loop to print out each name.

animals = ["cat", "lion", "tiger"]

#iterating and printing each animal
for animal in animals:
    
    #then print a statement about each animal
    print(f"A {animal.title()} is very dangerous but extremely cute. \n")    

print("Fun fact, these animals are all felines!")    